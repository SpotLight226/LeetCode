/**
 * @param {Function} fn
 * @param {Array} args
 * @param {number} t
 * @return {Function}
 */
var cancellable = function(fn, args, t) {
    // 예약된 timeout변수에다 fn에 변수args를 넣은 것과 시간 t를 저장하고
    var timeout = setTimeout(() => fn(...args), t);
    // 취소 함수인 clearTimeout에 timeout을 넣어서, cancelFn을 실행 시, timeout변수를 초기화한다
    var cancelFn = () => clearTimeout(timeout);

    return cancelFn
};

